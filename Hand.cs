namespace Janken
{
    // 【enum（イナム）とは？】
    // 「決まった選択肢」を名前で定義できる型
    // 例：信号は「赤・青・黄」の3つだけ → enumにぴったり！
    // じゃんけんの手も「グー・チョキ・パー」の3つだけ → enumにぴったり！
    //
    // enumを使わないと：0=グー、1=チョキ、2=パー と数字で管理 → わかりにくい！
    // enumを使うと：Hand.Rock、Hand.Scissors、Hand.Paper → 読みやすい！
    public enum Hand
    {
        // Rock    = グー   （岩・石のイメージ）
        // Scissors = チョキ （はさみのイメージ）
        // Paper   = パー   （紙のイメージ）
        Rock,       // 内部的には 0 として扱われる
        Scissors,   // 内部的には 1 として扱われる
        Paper       // 内部的には 2 として扱われる
    }

}